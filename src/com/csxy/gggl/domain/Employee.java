package com.csxy.gggl.domain;

public class Employee {
	String P_id;
	String P_name;
	String P_mobile;
	String P_address;
	int P_department;
	
	
	public String getP_id() {
		return P_id;
	}
	public void setP_id(String p_id) {
		P_id = p_id;
	}
	public String getP_name() {
		return P_name;
	}
	public void setP_name(String p_name) {
		P_name = p_name;
	}
	public String getP_mobile() {
		return P_mobile;
	}
	public void setP_mobile(String p_mobile) {
		P_mobile = p_mobile;
	}
	public String getP_address() {
		return P_address;
	}
	public void setP_address(String p_address) {
		P_address = p_address;
	}
	public int getP_department() {
		return P_department;
	}
	public void setP_department(int p_department) {
		P_department = p_department;
	}

}
