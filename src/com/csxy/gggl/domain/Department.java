package com.csxy.gggl.domain;

public class Department {
	int D_id;
	String D_name;
	int D_amount;
	public int getD_id() {
		return D_id;
	}
	public void setD_id(int d_id) {
		D_id = d_id;
	}
	public String getD_name() {
		return D_name;
	}
	public void setD_name(String d_name) {
		D_name = d_name;
	}
	public int getD_amount() {
		return D_amount;
	}
	public void setD_amount(int d_amount) {
		D_amount = d_amount;
	}
	
}
